# Suhu dalam celcius
celcius = 47

# rumus konversi suhu
fahrenheit = (celcius * 9/5) + 32
kelvin = celcius + 273.15
reamur = celcius * 4/5

# cetak nilai suhu
print("suhu dalam fahrenheit:", fahrenheit)
print("suhu dalam kelvin:", kelvin)
print("suhu dalam reamur:", reamur)